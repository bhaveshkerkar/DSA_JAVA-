import java.util.*;

public class switch1{
    public static void main(String args[]){
        int num = 3;

        switch(num){
            case 1 : System.out.println("samosa");
            break;
            case 2 : System.out.println("banana");
            break;
            case 3 : System.out.println("chineese");
            break;
            default : System.out.println("there is no item that I want");
        }
    }
}    